//     Start        //Finish    //Step Size
for (let count = 1; count < 11; count += 1) {
    console.log(count) // from 1 to 10
}
for (let count = 1; count < 11; count += 2) {
    console.log(count) //from 1 to 10 skipping 1 number
}
for (let count = 1; count < 21; count += 1) {
    console.log(count) //from 1 to 20
}
for (let count = 10; count < 21; count += 1) {
    console.log(count) //from 10 to 20
}
// using i as the variable in forloop is the convention used
for (let i = 10; i <= 100; i += 10) {
    console.log(i)
}